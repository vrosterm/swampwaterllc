create table
  public.carts (
    id integer generated by default as identity,
    customer_name text null,
    customer_class text null,
    level integer null default 0,
    constraint carts_pkey primary key (id)
  ) tablespace pg_default;

create table
  public.cart_items (
    id integer generated by default as identity,
    customer_id integer null,
    item_id integer null,
    quantity integer null,
    constraint cart_items_pkey primary key (id),
    constraint public_cart_items_customer_id_fkey foreign key (customer_id) references carts (id),
    constraint public_cart_items_item_id_fkey foreign key (item_id) references potion_inventory (id)
  ) tablespace pg_default;

  create table
  public.gold_ledger (
    id integer generated by default as identity,
    created_at timestamp with time zone not null default now(),
    change integer null default 0,
    "desc" integer null,
    constraint gold_ledger_pkey primary key (id)
  ) tablespace pg_default;

  create table
  public.ml_ledger (
    id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    type text null,
    change integer not null default 0,
    constraint ml_ledger_pkey primary key (id)
  ) tablespace pg_default;

  create table
  public.potion_ledger (
    id bigint generated by default as identity,
    created_at timestamp with time zone not null default now(),
    potion_id integer null,
    change integer not null default 0,
    "desc" integer null,
    constraint ledger_entries_pkey primary key (id)
  ) tablespace pg_default;